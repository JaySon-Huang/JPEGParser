    public Historgram[] getHistorgram(){
        Historgram[] ret = new Historgram[64];
        for (int i = 0; i != 64; ++i){
            ret[i] = new Historgram();
        }
        for (int[] unit : mUnits){
            for (int i = 0; i != 64; ++i){
                ret[i].addN( unit[i] );
            }
        }
        return ret;
    }

